export function getSymbols(count: number) {
	const copy = symbols.slice();
	const randomSymbols = [];
	while (randomSymbols.length < count) {
		randomSymbols.push(copy[Math.floor(Math.random() * symbols.length)]);
	}
	return randomSymbols;
}
